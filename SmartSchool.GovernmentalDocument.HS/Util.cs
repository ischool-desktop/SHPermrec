        /// <summary>
        /// 使用學校代碼判斷日進校
        /// </summary>
        /// <returns></returns>
        public static string GetSchoolType1()
        {
            // 使用學校代碼第4碼還看
            string retVal = "日校";
            int i;
            string SchoolCode = K12.Data.School.Code;
            if (!string.IsNullOrEmpty(SchoolCode))
                if (SchoolCode.Length > 4)
                    if (int.TryParse(SchoolCode.Substring(3, 1), out i))
                        retVal = "日校";
                    else
                        retVal = "進校";

            return retVal;
        }

        /// <summary>
        /// 取得學生年級
        /// </summary>
        /// <param name="StudIDList"></param>
        /// <returns></returns>
        public static Dictionary<string, string> GetStudentGradeYearDict(List<string> StudIDList)
        {
            Dictionary<string, string> retVal = new Dictionary<string, string>();
            foreach (SHStudentRecord stud in SHStudent.SelectByIDs(StudIDList ))
            {
                if (stud.Class != null)
                    if (stud.Class.GradeYear.HasValue)
                        if (!retVal.ContainsKey(stud.ID))
                            retVal.Add(stud.ID, stud.Class.GradeYear.Value.ToString());
            }

            return retVal;
        }

        /// <summary>
        /// 取得學生特殊代碼
        /// </summary>
        /// <param name="StudIDList"></param>
        /// <returns></returns>
        public static Dictionary<string, string> GetStudSpacCodeDict(List<string> StudIDList)
        {
            Dictionary<string, string> retVal = new Dictionary<string, string>();

            // 取得有學生特殊代碼
            List<SHUpdateRecordRecord> StudUpRecList = SHUpdateRecord.SelectByStudentIDs(StudIDList).Where(x => x.SpecialStatus != "").ToList();

            foreach (SHUpdateRecordRecord rec in StudUpRecList)
            {
                if (retVal.ContainsKey(rec.StudentID))
                {
                    if(retVal[rec.StudentID].IndexOf(rec.SpecialStatus) <0)
                        retVal[rec.StudentID] = retVal[rec.StudentID] + "," + rec.SpecialStatus;                
                }
                else
                {
                    retVal.Add(rec.StudentID,rec.SpecialStatus );
                }            
            }

            return retVal;
        }

        /// <summary>
        /// 將日期轉換格式 2010/1/1 -> 990101
        /// </summary>
        /// <param name="strDate"></param>
        /// <returns></returns>
        public static string ConvertDateStr1(string strDate)
        {
            string retVal=string.Empty;
            DateTime dt;

            if(DateTime.TryParse(strDate ,out dt))
            {
                retVal = "" + (dt.Year - 1911) + string.Format("{0:00}", dt.Month) + string.Format("{0:00}", dt.Day);            
            }
            return retVal;        
        }

        /// <summary>
        /// 將日期年轉成民國年 2010/1/1->99/1/1
        /// </summary>
        /// <param name="strDate"></param>
        /// <returns></returns>
        public static string ConvertDateStr2(string strDate)
        {
            string retVal = string.Empty;
            DateTime dt;
            if(DateTime.TryParse(strDate ,out dt))
            {
                if(dt.Year >1000)
                    retVal =(dt.Year-1911)+"/"+dt.Month+"/"+dt.Day;
            }
            return retVal;
        }

        /// <summary>
        /// 取得字號字前文字
        /// </summary>
        /// <param name="strDoc"></param>
        /// <returns></returns>
        public static string GetUpdateDocNumDoc(string strDocNum)
        {
            
            string retVal = string.Empty;
            int idx=strDocNum.IndexOf("字");
            if (idx > 0)
                retVal = strDocNum.Substring(0, idx);

            return retVal;        
        }

        /// <summary>
        /// 取得字號字間數字
        /// </summary>
        /// <param name="strDocNum"></param>
        /// <returns></returns>
        public static string GetUpdateDocNumNum(string strDocNum)
        {
            string retVal = string.Empty;
            int beginIdx = strDocNum.IndexOf("第")+1;
            int MovIdx = strDocNum.IndexOf("號")-beginIdx;

            if (beginIdx >= 0 && MovIdx >= 0)
                retVal = strDocNum.Substring(beginIdx, MovIdx);

            return retVal;
        }

